import math
# 1. Find the square root of 625
print("Square root of 625:", math.sqrt(625))

# 2. Calculate the factorial of 6
print("Factorial of 6:", math.factorial(6))

# 3. Find the floor and ceiling values of 4.7
print("Floor of 4.7:", math.floor(4.7))
print("Ceil of 4.7:", math.ceil(4.7))

# 4. Compute 5 raised to the power 3 using math module
print("5^3:", math.pow(5, 3))

# 5. Find the absolute value of -18 using math.fabs()
print("Absolute value of -18:", math.fabs(-18))

# 6. Find sine, cosine, and tangent of 90 degrees
radians = math.radians(90)
print("Sin of 90 degrees:", math.sin(radians))
print("Cos of 90 degrees:", math.cos(radians))
print("Tan of 90 degrees:", math.tan(radians))

# 7. Find the GCD of 36 and 60
print("GCD of 36 and 60:", math.gcd(36, 60))

# 8. Find the value of e^3 using math module
print("e^3:", math.exp(3))

# 9. Find the logarithm base 10 of 1000
print("Log10 of 1000:", math.log10(1000))

# 10. Convert 180 degrees into radians
print("180 degrees in radians:", math.radians(180))

